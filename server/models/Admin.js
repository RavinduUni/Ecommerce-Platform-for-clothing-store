import mongoose from "mongoose";

const adminScema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
        unique: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
}, { timestamps: true });

const Admin = mongoose.model("Admin", adminScema);

export default Admin;